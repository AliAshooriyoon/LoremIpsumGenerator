const inputValue = document.querySelector("#inputNumber");
const selectTypes = document.querySelector("#selectTypes");
const button = document.getElementById("submit");
let output = document.getElementById("text");
let copyButton = document.getElementById("copy");
button.addEventListener("click", submit);
function submit() {
  //Dieses Event ist für unseren Button
  if (inputValue.value.length < 4 && selectTypes.value) {
    console.log(true);
    if (selectTypes.value === "Wörter") {
      console.log(
        text.join(" ").split(" ").splice(0, inputValue.value).join(" "),
      );
      output.innerHTML = text
        .join(" ")
        .split(" ")
        .splice(0, inputValue.value)
        .join(" ");
    } else if (selectTypes.value === "Zeichen") {
      console.log(
        text
          .join(" ")
          .split(" ")
          .join("")
          .split("")
          .splice(0, inputValue.value)
          .join(""),
      );
      output.innerHTML = text
        .join(" ")
        .split(" ")
        .join("")
        .split("")
        .splice(0, inputValue.value)
        .join("");
    } else if (selectTypes.value === "Absätze") {
      console.log("ok!");
      console.log(
        text.join("").split(".").splice(0, inputValue.value).join(""),
      );
      output.innerHTML = text
        .join("")
        .split(".")
        .splice(0, inputValue.value)
        .join("");
    }
  } else {
    alert("Probiers alle Dateien! (input.length<4)");
  }
}
copyButton.addEventListener("click", () => {
  output.select();
  output.setSelectionRange(0, 99999);
  document.execCommand("copy");
});
